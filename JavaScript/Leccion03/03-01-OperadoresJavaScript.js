//OperadoresJavaScript

let a = 3, b = 2;
let z = a + b;
console.log("Resultado de la suma: "+ z);

z = a - b;
console.log("Resultado de la resta: "+ z);

z = a * b;
console.log("Resultado de la multiplicaci√≥n: "+ z);

z = a / b;
console.log("Resultado de la divicion: "+ z);

z = a % b;
console.log("Resultado de operacion modulo (residuo): "+ z);

z = a ** b;
console.log("Resultado de operador exponente: "+ z);

// Incremento
// Pre-incremento (el operador ++ antes de la varible)
z = ++a;
console.log(a);
console.log(z);

//Post-incremento (el operador ++ despues de la varible)

z = b++;
console.log(b);
console.log(z);

//Decremento
//Pre-decremento
z = --a;
console.log(a);
console.log(z);

//Post-decremento
z = b--;
console.log(b);
console.log(z);

